-- Idempotency
--   - ALTER TABLE IF EXISTS, ALTER INDEX IF EXISTS (PG 15+)
--   - DROP POLICY / TRIGGER / FUNCTION IF EXISTS
--   - Enum type + enum value renames wrapped in DO blocks with
--     EXCEPTION clauses (no native IF EXISTS for those).
--   - ALTER TABLE … RENAME CONSTRAINT wrapped in a DO block with
--     an EXCEPTION handler (no native IF EXISTS for that sub-
--     command).
--   - Trigger + policy recreations: no IF NOT EXISTS in Postgres.
--     Belt-and-suspenders drop-before-create of the NEW names in
--     §7 and §8 (on top of the drops of the OLD names in §3/§4)
--     makes the whole migration safely re-runnable.
--   - Function replacement uses DROP + CREATE (not ALTER RENAME)
--     because the function body carries the old table name in a
--     RAISE EXCEPTION literal that must be rewritten.
--
-- Rollback (manual; SQL in comment block at end of file). The
-- checkpoint tag also lets git-level rollback to pre-P2 state if
-- the full 4-commit rename sequence needs unwinding. Rollback is
-- itself fully idempotent (same DO / IF EXISTS / drop-before-
-- create pattern as the forward migration). Rollback ordering is
-- symmetric to forward: §6b reverse precedes §6a reverse which
-- precedes §6 reverse, because ALTER TABLE RENAME CONSTRAINT
-- references the table by its current name.
