BEGIN;


-- ┌─────────────────────────────────────────────────────────────┐
-- │  §1  Rename enum types                                      │
-- │                                                             │
-- │  Column definitions using these types reference them by     │
-- │  OID, so all status/event_type/auto_cancel_reason columns   │
-- │  follow the rename transparently.                           │
-- └─────────────────────────────────────────────────────────────┘

DO $$ BEGIN
  ALTER TYPE direct_offer_status RENAME TO special_offer_status;
EXCEPTION WHEN undefined_object THEN NULL;
END $$;

DO $$ BEGIN
  ALTER TYPE direct_offer_event_type RENAME TO special_offer_event_type;
EXCEPTION WHEN undefined_object THEN NULL;
END $$;

DO $$ BEGIN
  ALTER TYPE direct_offer_auto_cancel_reason RENAME TO special_offer_auto_cancel_reason;
EXCEPTION WHEN undefined_object THEN NULL;
END $$;


-- ┌─────────────────────────────────────────────────────────────┐
-- │  §2  Rename the 'direct_offer' value on licence_source_type │
-- │                                                             │
-- │  licence_grants.source_type and transactions.source_type    │
-- │  rows currently storing 'direct_offer' are atomically       │
-- │  migrated to 'special_offer' by this statement — Postgres   │
-- │  stores enum values as OIDs, so no UPDATE is needed.        │
-- └─────────────────────────────────────────────────────────────┘

DO $$ BEGIN
  ALTER TYPE licence_source_type RENAME VALUE 'direct_offer' TO 'special_offer';
EXCEPTION WHEN invalid_parameter_value THEN NULL;
END $$;


-- ┌─────────────────────────────────────────────────────────────┐
-- │  §3  Drop RLS policies whose names reference direct_offer   │
-- │                                                             │
-- │  Policy bodies work via OID internally (they would continue │
-- │  to function against the renamed tables), but the policy    │
-- │  NAMES would be stale. Drop + recreate with new names in §8.│
-- │  offer_checkout_intents_participant_read is untouched — its │
-- │  name and body are independent of the direct_offer naming.  │
-- └─────────────────────────────────────────────────────────────┘

DROP POLICY IF EXISTS direct_offer_threads_participant_read
  ON direct_offer_threads;

DROP POLICY IF EXISTS direct_offer_events_participant_read
  ON direct_offer_events;


-- ┌─────────────────────────────────────────────────────────────┐
-- │  §4  Drop triggers that will be recreated in §7             │
-- │                                                             │
-- │  Triggers reference the function by name at CREATE time;    │
-- │  they must be dropped before §5 drops the function they     │
-- │  depend on.                                                 │
-- └─────────────────────────────────────────────────────────────┘

DROP TRIGGER IF EXISTS trg_direct_offer_events_no_update
  ON direct_offer_events;

DROP TRIGGER IF EXISTS trg_direct_offer_events_no_delete
  ON direct_offer_events;


-- ┌─────────────────────────────────────────────────────────────┐
-- │  §5  Replace the immutable-enforcement function             │
-- │                                                             │
-- │  The original function body contained a RAISE EXCEPTION     │
-- │  literal that named the old table:                          │
-- │    'direct_offer_events is append-only. …'                  │
-- │  A simple ALTER FUNCTION RENAME would leave that string     │
-- │  in place — user-visible and now stale. Drop + create       │
-- │  under the new name lets us rewrite the literal cleanly.    │
-- └─────────────────────────────────────────────────────────────┘

DROP FUNCTION IF EXISTS direct_offer_events_immutable();

CREATE OR REPLACE FUNCTION special_offer_events_immutable()
RETURNS TRIGGER AS $$
BEGIN
  RAISE EXCEPTION 'special_offer_events is append-only. % operations are forbidden.', TG_OP;
  RETURN NULL;
END;
$$ LANGUAGE plpgsql;


-- ┌─────────────────────────────────────────────────────────────┐
-- │  §6  Rename the tables                                      │
-- │                                                             │
-- │  Indexes, PKs, CHECK constraints, inline FKs, and FKs with  │
-- │  generic-prefix names (fk_offer_threads_*) all auto-follow  │
-- │  the table rename via OID references. Most of those objects │
-- │  also have names that do NOT contain "direct_offer", so     │
-- │  they need no explicit rename. The exceptions are the 3     │
-- │  auto-named constraints handled in §6a and §6b below.       │
-- └─────────────────────────────────────────────────────────────┘

ALTER TABLE IF EXISTS direct_offer_threads RENAME TO special_offer_threads;
ALTER TABLE IF EXISTS direct_offer_events  RENAME TO special_offer_events;


-- ┌─────────────────────────────────────────────────────────────┐
-- │  §6a Rename PK constraint names                             │
-- │                                                             │
-- │  PK constraints created by inline `id uuid PRIMARY KEY …`   │
-- │  in the original CREATE TABLE statements carry auto-names   │
-- │  `<table>_pkey`. ALTER TABLE … RENAME TO does NOT rename    │
-- │  associated constraint names, so the PKs are now attached   │
-- │  to the renamed tables but carry stale direct_offer_* names.│
-- │                                                             │
-- │  PK constraints share a name with their backing unique      │
-- │  index; ALTER INDEX RENAME updates both atomically.         │
-- └─────────────────────────────────────────────────────────────┘

ALTER INDEX IF EXISTS direct_offer_threads_pkey
  RENAME TO special_offer_threads_pkey;

ALTER INDEX IF EXISTS direct_offer_events_pkey
  RENAME TO special_offer_events_pkey;


-- ┌─────────────────────────────────────────────────────────────┐
-- │  §6b Rename the auto-named inline FK constraint             │
-- │                                                             │
-- │  The constraint was created implicitly by                   │
-- │    thread_id uuid NOT NULL REFERENCES direct_offer_threads… │
-- │  in the original direct_offer_events table definition.      │
-- │  Postgres names inline FKs `<table>_<column>_fkey`, so the  │
-- │  constraint is now attached to the renamed table            │
-- │  (special_offer_events) but carries the old name. Rename    │
-- │  explicitly.                                                │
-- │                                                             │
-- │  ALTER TABLE … RENAME CONSTRAINT has no native IF EXISTS;   │
-- │  wrap in a DO block with EXCEPTION handler for re-run       │
-- │  safety.                                                    │
-- └─────────────────────────────────────────────────────────────┘

DO $$ BEGIN
  ALTER TABLE special_offer_events
    RENAME CONSTRAINT direct_offer_events_thread_id_fkey
    TO special_offer_events_thread_id_fkey;
EXCEPTION WHEN undefined_object THEN NULL;
END $$;


-- ┌─────────────────────────────────────────────────────────────┐
-- │  §7  Recreate triggers on renamed table with new names      │
-- │                                                             │
-- │  Belt-and-suspenders drops of the NEW names make the        │
-- │  CREATE TRIGGER statements safe to re-run (Postgres has no  │
-- │  CREATE TRIGGER IF NOT EXISTS).                             │
-- └─────────────────────────────────────────────────────────────┘

DROP TRIGGER IF EXISTS trg_special_offer_events_no_update
  ON special_offer_events;

DROP TRIGGER IF EXISTS trg_special_offer_events_no_delete
  ON special_offer_events;

CREATE TRIGGER trg_special_offer_events_no_update
  BEFORE UPDATE ON special_offer_events
  FOR EACH ROW
  EXECUTE FUNCTION special_offer_events_immutable();

CREATE TRIGGER trg_special_offer_events_no_delete
  BEFORE DELETE ON special_offer_events
  FOR EACH ROW
  EXECUTE FUNCTION special_offer_events_immutable();


-- ┌─────────────────────────────────────────────────────────────┐
-- │  §8  Recreate RLS policies with new names                   │
-- │                                                             │
-- │  §8.1 body preserved byte-for-byte (no table name inside).  │
-- │  §8.2 body has the FROM clause re-texted to the new table   │
-- │       name (direct_offer_threads → special_offer_threads).  │
-- │       Semantics identical.                                  │
-- │                                                             │
-- │  Belt-and-suspenders drops of the NEW policy names make the │
-- │  CREATE POLICY statements safe to re-run (Postgres has no   │
-- │  CREATE POLICY IF NOT EXISTS).                              │
-- └─────────────────────────────────────────────────────────────┘

DROP POLICY IF EXISTS special_offer_threads_participant_read
  ON special_offer_threads;

DROP POLICY IF EXISTS special_offer_events_participant_read
  ON special_offer_events;

CREATE POLICY special_offer_threads_participant_read
  ON special_offer_threads
  FOR SELECT TO authenticated
  USING (
    buyer_id = auth.uid()
    OR creator_id = auth.uid()
  );

CREATE POLICY special_offer_events_participant_read
  ON special_offer_events
  FOR SELECT TO authenticated
  USING (EXISTS (
    SELECT 1 FROM special_offer_threads t
    WHERE t.id = special_offer_events.thread_id
      AND (t.buyer_id = auth.uid() OR t.creator_id = auth.uid())
  ));


-- ┌─────────────────────────────────────────────────────────────┐
-- │  §9  Update table-level COMMENTs that mentioned the old     │
-- │      "Direct Offer" prose (grep honesty at the schema       │
-- │      documentation layer). Column-level comments in the     │
-- │      original migrations describe functionality, not the    │
-- │      product name, and transfer unchanged via OID.          │
-- └─────────────────────────────────────────────────────────────┘

COMMENT ON TABLE special_offer_threads IS
  'Master Special Offer negotiation thread. One active thread per buyer × asset × licence. Spec §10.4.';

COMMENT ON TABLE special_offer_events IS
  'Append-only event log for Special Offer lifecycle. No UPDATE or DELETE permitted.';


COMMIT;


